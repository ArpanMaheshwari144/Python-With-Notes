# Creating a list using []
a = [1, 2, 4, 56, 6]

# Print the list using print() function
print(a)

# Access using index using a[0], a[1],....
print(a[0])
print(a[1])
print(a[2])
print(a[3])
print(a[4])

# Changing the values of the list
a[0] = 98
print(a)

# We can create a list with items of different types
b = [45, "Arpan", False, 7.88]
print(b)